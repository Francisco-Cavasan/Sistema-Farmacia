    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int cod = codCat.getInteger();
        String nome = name.getText();
        if(cod > 0 && !(nome.isBlank())){
            Categoria categoria = new Categoria(cod,nome );
            try {
                dao.create(categoria);
            } catch (DataBaseException | DuplicateKeyException ex) {
               Warning w = new Warning("Não foi possível criar este registro. Confira os dados inseridos.");
               w.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

